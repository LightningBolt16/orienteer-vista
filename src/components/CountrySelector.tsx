// Helper function to get flag by code (returns emoji for fallback)
export const getCountryFlag = (code: string | null | undefined): string => {
  if (!code) return '';
  const country = COUNTRIES.find(c => c.code === code);
  return country?.flag || '';
};

// Helper component to render flag as an image (works on Windows)
export const CountryFlagImage: React.FC<{ code: string; size?: number; className?: string }> = ({ code, size = 20, className = '' }) => {
  const lowerCode = code.toLowerCase();
  return (
    <img
      src={`https://flagcdn.com/w40/${lowerCode}.png`}
      srcSet={`https://flagcdn.com/w80/${lowerCode}.png 2x`}
      width={size}
      height={Math.round(size * 0.75)}
      alt={getCountryName(code)}
      className={`inline-block ${className}`}
      style={{ verticalAlign: 'middle' }}
      loading="lazy"
    />
  );
};

// Helper function to get country name by code
export const getCountryName = (code: string | null | undefined): string => {
  if (!code) return '';
  const country = COUNTRIES.find(c => c.code === code);
  return country?.name || code;
};

interface CountrySelectorProps {
  value?: string | null;
  onChange: (code: string) => void;
  placeholder?: string;
  className?: string;
  disabled?: boolean;
}

const CountrySelector: React.FC<CountrySelectorProps> = ({
  value,
  onChange,
  placeholder,
  className,
  disabled = false,
}) => {
  const { t } = useLanguage();
  const [open, setOpen] = useState(false);
  const [searchQuery, setSearchQuery] = useState('');

  const selectedCountry = useMemo(() => {
    return COUNTRIES.find(c => c.code === value);
  }, [value]);

  const filteredCountries = useMemo(() => {
    if (!searchQuery) return COUNTRIES;
    const query = searchQuery.toLowerCase();
    return COUNTRIES.filter(c => 
      c.name.toLowerCase().includes(query) || 
      c.code.toLowerCase().includes(query)
    );
  }, [searchQuery]);

  return (
    <Popover open={open} onOpenChange={setOpen}>
      <PopoverTrigger asChild>
        <Button
          variant="outline"
          role="combobox"
          aria-expanded={open}
          className={cn("w-full justify-between", className)}
          disabled={disabled}
        >
          {selectedCountry ? (
            <span className="flex items-center gap-2">
              <CountryFlagImage code={selectedCountry.code} size={20} />
              <span>{selectedCountry.name}</span>
            </span>
          ) : (
            <span className="text-muted-foreground">
              {placeholder || t('selectCountry') || 'Select country...'}
            </span>
          )}
          <ChevronsUpDown className="ml-2 h-4 w-4 shrink-0 opacity-50" />
        </Button>
      </PopoverTrigger>
      <PopoverContent className="w-[300px] p-0" align="start">
        <Command shouldFilter={false}>
          <CommandInput 
            placeholder={t('searchCountry') || 'Search country...'} 
            value={searchQuery}
            onValueChange={setSearchQuery}
          />
          <CommandList>
            <CommandEmpty>{t('noCountryFound') || 'No country found.'}</CommandEmpty>
            <CommandGroup className="max-h-[300px] overflow-auto">
              {filteredCountries.map((country) => (
                <CommandItem
                  key={country.code}
                  value={country.code}
                  onSelect={() => {
                    onChange(country.code);
                    setOpen(false);
                    setSearchQuery('');
                  }}
                >
                  <Check
                    className={cn(
                      "mr-2 h-4 w-4",
                      value === country.code ? "opacity-100" : "opacity-0"
                    )}
                  />
                  <CountryFlagImage code={country.code} size={20} className="mr-2" />
                  <span>{country.name}</span>
                </CommandItem>
              ))}
            </CommandGroup>
          </CommandList>
        </Command>
      </PopoverContent>
    </Popover>
  );
};

export default CountrySelector;
